    is_coverage_question = any(word in query for word in ["coverage", "cover", "covers", "covered", "protect", "protection"])
    
    # Check for insurance types
    is_motor_insurance = any(word in query for word in ["motor", "auto", "car", "vehicle", "automobile"])
    is_life_insurance = "life" in query and "insurance" in query
    is_health_insurance = "health" in query and "insurance" in query
    is_home_insurance = any(word in query for word in ["home", "house", "property"]) and "insurance" in query
    
    # Match the appropriate response
    if is_motor_insurance:
        if is_apply_question:
            return MOTOR_INSURANCE_APPLY
        elif is_coverage_question:
            return MOTOR_INSURANCE_COVERAGE
        else:
            return MOTOR_INSURANCE_GENERAL
    elif is_life_insurance:
        if is_apply_question:
            return LIFE_INSURANCE_APPLY
        else:
            return LIFE_INSURANCE_GENERAL
    elif is_health_insurance:
        if is_apply_question:
            return HEALTH_INSURANCE_APPLY
        else:
            return HEALTH_INSURANCE_GENERAL
    elif is_home_insurance:
        if is_apply_question:
            return HOME_INSURANCE_APPLY
        else:
            return HOME_INSURANCE_GENERAL
    elif "insurance" in query:
        if is_apply_question:
            return GENERAL_INSURANCE_APPLY
        else:
            return GENERAL_INSURANCE_INFO
    else:
        return DEFAULT_RESPONSE